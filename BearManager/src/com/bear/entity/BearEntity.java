package com.bear.entity;

public class BearEntity {
	private int agn;
	private String name;
	private String type;
	public int getAgn() {
		return agn;
	}
	public void setAgn(int agn) {
		this.agn = agn;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	
	
}
